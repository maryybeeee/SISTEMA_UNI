DROP DATABASE IF EXISTS bd;
CREATE DATABASE bd;
USE bd;

CREATE TABLE profesores (
  NoTrabajador INT NOT NULL,
  Nombres VARCHAR(100) NOT NULL,
  Clave VARCHAR(60) NOT NULL,
  PRIMARY KEY (NoTrabajador)
);

CREATE TABLE jornadas_academicas (
  NoControl INT NOT NULL,
  ApellidoP VARCHAR(25) NOT NULL,
  ApellidoM VARCHAR(25) NOT NULL,
  Nombres VARCHAR(50) NOT NULL,
  `2019` VARCHAR(255) NULL,
  `2021_A` VARCHAR(255) NULL,
  `2021_B` VARCHAR(255) NULL,
  `2022` VARCHAR(255) NULL,
  `2023` VARCHAR(255) NULL,
  Conteo INT NOT NULL DEFAULT 0,
  Estado ENUM('LIBERADO','NO LIBERADO') NOT NULL,
  Atendidos ENUM('ATENDIDO','NO ATENDIDO') NOT NULL,
  PRIMARY KEY (NoControl)
);

CREATE TABLE rutas_pdf (
  id INT AUTO_INCREMENT PRIMARY KEY,
  no_control INT NOT NULL,
  nombre_archivo VARCHAR(255) NOT NULL,
  ruta_archivo VARCHAR(255) NOT NULL,
  tabla_referencia VARCHAR(255) NOT NULL,
  FOREIGN KEY (no_control) REFERENCES jornadas_academicas(NoControl) ON DELETE CASCADE
);

CREATE TABLE alumnos (
    id INT AUTO_INCREMENT PRIMARY KEY,
    Nombres VARCHAR(50) NOT NULL,
    ApellidoP VARCHAR(25) NOT NULL,
    ApellidoM VARCHAR(25) NOT NULL,
    Contraseña VARCHAR(100) NOT NULL,
    NoControl INT UNIQUE NOT NULL,
    FOREIGN KEY (NoControl) REFERENCES jornadas_academicas(NoControl)
);

